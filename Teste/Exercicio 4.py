dia = input('Digite um valor entre 1 e 7: ')

if dia == '1':
  print('Segunda.')

elif dia == '2':
  print('Terça')
  
elif dia == '3':
  print('Quarta.')

elif dia == '4':
  print('Quinta-Feira.')

elif dia == '5':
  print('Sexta-Feira.')

elif dia == '6':
  print('Sábado.')

elif dia == '7':
  print('Domingo.')

else:
  print('Dia inválido.')